package inheritance;

public class Parent {
	
	public void test1() {
		
		System.out.println("In Super class");
	}
	
	public void test2() {
		
		System.out.println("In super class in method test 2");
	}
	
	private void test3() {
		
		System.out.println("In test 3 - Super class");
	}
	
	protected void test4()
	{
		System.out.println("in super class protected method");
	}
	
	
	

}
